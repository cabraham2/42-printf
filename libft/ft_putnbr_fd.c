#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	if (nb == 2147483647)
	{
		write(fd, "2147483647", 10);
		return ;
	}
	if (nb == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb *= -1;
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
	{
		nb += '0';
		write(fd, &nb, 1);
	}
}
